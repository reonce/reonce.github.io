<h2 id="_6-演讲能力" tabindex="-1"><a class="header-anchor" href="#_6-演讲能力" aria-hidden="true">#</a> 6 演讲能力</h2>
<p>一场好的演讲通常由一下几部分组成：</p>
<ol>
<li>提前了解听众的诉求：针对面向的群体类型，以及听众的诉求来设计这一次的演讲</li>
<li>好的开场是成功的一半：开场可以来一些小互动以及调研等等，把听众的思绪带进到分享主题里来</li>
<li>好的内容结构：参考「结构化思维」的内容</li>
<li>PPT 只是配角：把结构内容呈现到 PPT 上，切记不要把一个好的演讲全部归功到 PPT 上</li>
<li>把控好整体节奏：思考如何让听众跟着你的思路走，一旦跟不上，就算不上号的演讲</li>
<li>趣味性的互动：合适的情况下可以来一些互动，避免单向输入带来的乏味</li>
<li>肢体语言：好的肢体语言可以让演讲更生动</li>
<li>声音控制：流利的表达、抑扬顿挫的表达会让人更能接受你的信息</li>
<li>收尾时强调重点：跟写文章的中心思想类似，把听众的思绪拉倒中心思想上来</li>
</ol>
<h2 id="_7-协同-领导能力" tabindex="-1"><a class="header-anchor" href="#_7-协同-领导能力" aria-hidden="true">#</a> 7. 协同/领导能力</h2>
<p>在工作中我们基本上都会遇到团队内部协同、跨团队协同、以及跨部门协同的情况，特别是到了一定的层级，做项目管理、团队管理之后，协同与领导能力更为重要，我们也称为「借人成事」的能力。这里我们要学习很多协同领导相关的能力，比如：</p>
<ul>
<li>项目管理（预期、过程、质量、风险）：项目管理也是一个比较专业，每个程序员必学</li>
<li>开好会：当需要协同的角色越多，开会的效率就变得尤其重要，在大厂有多少程序员的时间是花在了开会上，这部分建议参考集《高效会议指南》</li>
<li>复好盘：柳传志在《我的复盘方法论》中说到：在这些年管理工作和自我成长中，「复盘」是最令我受益的工具之一，可见复盘对人成长的重要性，这里推荐一本书叫做 《<a href="https://link.zhihu.com/?target=https%3A//book.douban.com/subject/20485545/" target="_blank" rel="noopener noreferrer">复盘+：把经验转化为能力<ExternalLinkIcon/></a>》</li>
<li>做到事事有回应、件件有着落、凡事有交代：做一个靠谱的职场人，三个最重要的要素</li>
<li>诚信：说到做到，提升信任感</li>
<li>保持正能量：多传递一些正能量，谣言与八卦在职场中常常被人津津乐道，但往往也会影响我们的职业发展</li>
<li>严于律己，宽以待人：领导者必备</li>
</ul>
<h2 id="_8-抗压能力" tabindex="-1"><a class="header-anchor" href="#_8-抗压能力" aria-hidden="true">#</a> 8. 抗压能力</h2>
<p>人的一生中不可能不会遇到压力和挫折，抗压能力的高低，会影响我们的生活与工作，抗压能力越高，我们越容易适应当下的环境，所以说，提高抗压能力是非常有必要的，这里可以给一些提升抗压能力的建议，也是我个人在工作之外会经常保持的习惯：</p>
<ul>
<li>培养兴趣爱好：把兴趣爱好作为自己的灵魂伴侣</li>
<li>有三五好友可以交流与倾诉：遇到压力和挫折的时候，身边如果有人能够相互鼓励，压力会小很多</li>
<li>保证充足的睡眠：很多时候，我们遇到压力和挫折的时候会失眠，但我们尽量在挫折不出现的时候就保证充足的睡眠的习惯，压力来的时候不至于影响太大，压力越大越无法入睡，会进入一个恶性循环。（这里没有理论做支撑，自己的感受）</li>
<li>保持运动习惯：保持对自己兴趣的运动习惯，和三五好友打球，充足睡眠自然来</li>
</ul>
